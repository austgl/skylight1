package org.skylight1.neny.android.database.model;

public enum Cuisine {
	
	AFRICAN("African"),
	ASIAN("Asian"),
	CENTRAL_AND_SOUTH_AMERICAN("South American"),
	COMFORT_AND_SNACKS("Comfort"),
	EUROPEAN("European"),
	ITALIAN("Italian"),
	MIDDLE_EASTERN("Middle Eastern"),
	US_NORTH_AMERICAN("North American"),
	INDIAN_SUBCONTINENT("Indian"),
	ECLECTIC("Eclectic"),
	PACIFICA("Pacifica"),
	VEGETARIAN("Vegetarian");

	private final String label;

	private Cuisine(String aLabel) {
		label = aLabel;
	}

	public String getLabel() {
		return label;
	}
}
